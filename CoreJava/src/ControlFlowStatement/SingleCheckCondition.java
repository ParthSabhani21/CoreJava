package ControlFlowStatement;

public class SingleCheckCondition 
{
	public static void main(String[] args) 
	{
		int a = 8, b = 5;
		
		// condition
		if(a > b)
		{
			System.out.println("a > b");
		}
		
		if(a == b)
		{
			System.out.println("a = b");
		}
		
		if(a < b)
		{
			System.out.println("a < b");
		}
	}
}
